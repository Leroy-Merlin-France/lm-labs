package com.leroymerlin.corp.fr.nuxeo.labs.site.exception;

public class ThemePropertiesException extends Exception {

    private static final long serialVersionUID = 1L;

    public ThemePropertiesException() {
        super();
    }

    public ThemePropertiesException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public ThemePropertiesException(String arg0) {
        super(arg0);
    }

    public ThemePropertiesException(Throwable arg0) {
        super(arg0);
    }

}
